    private void aceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aceptarActionPerformed
        aceptar.addActionListener((e) -> {
            char caracter=0;
            cadena=Ingresar_texto.getText();
            cadena=cadena.toLowerCase();
            int contador=0;
            String nueva=cadena;
            while (cadena.length() != 0) {
                int contadorAux = 0;
                for (int j = 0; j < cadena.length(); j++) {
                    if (cadena.charAt(0) == cadena.charAt(j)) {
                        contadorAux++;
                    }
                }
                if (contadorAux > contador) {
                    contador = contadorAux;
                    caracter = cadena.charAt(0);
                    
                }
                cadena = cadena.replaceAll(cadena.charAt(0) + "", "");
            }

            caracter_repetido.setText(Character.toString(caracter));
            veces.setText(Integer.toString(contador));

            nueva = nueva.replace('a',caracter).replace('e',caracter).replace('i',caracter).replace('o',caracter).replace('u',caracter);

            mostrarr.setText(nueva);
        });
    }//GEN-LAST:event_aceptarActionPerformed

